package com.xdcs.dao;

import java.util.List;

import com.xdcs.vo.Goods;

/**
 * 
 * 商品数据操作接口
 * 
 * @author Administrator
 * 
 */
public interface IGoodDao {
	/**
	 * 查询所有的商品
	 * 
	 * @return 商品的集合
	 */
	public abstract List<Goods> findAll();

	/**
	 * 添加商品
	 * 
	 * @return
	 */
	public abstract boolean addGoods(Goods goods);

	/**
	 * 删除商品
	 * 
	 * @param goods
	 * @return
	 */
	public abstract boolean deleteGoods(Goods goods);
}
